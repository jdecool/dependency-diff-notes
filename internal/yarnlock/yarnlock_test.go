func TestParse(t *testing.T) {
	tests := []struct {
		name    string
		file    string
		want    lockfile.Lock
		wantErr bool
	}{
		{
			name: "classic: realistic lock file, scoped package, multiple spec keys collapse to one entry",
			file: "testdata/classic_realistic.lock",
			want: lockfile.Lock{
				Combined: true,
				Packages: []lockfile.Package{
					{Name: "@babel/code-frame", Version: "7.12.13"},
					{Name: "lodash", Version: "4.17.21"},
				},
			},
		},
		{
			name: "classic: git dependency reference extracted from resolved, registry tarball hash is not mistaken for one",
			file: "testdata/classic_git_dependency.lock",
			want: lockfile.Lock{
				Combined: true,
				Packages: []lockfile.Package{
					{Name: "libsignal", Version: "2.0.1", Reference: "11dbd962ea108187c79a7c46fe4d6f790e23da97"},
					{Name: "tooling", Version: "1.26.4", Reference: "978dc1c9680ef7d79f5f5c02c3439385d7937c39"},
				},
			},
		},
		{
			name: "classic: header only, no packages",
			file: "testdata/classic_empty.lock",
			want: lockfile.Lock{Combined: true},
		},
		{
			name: "berry: realistic lock file, name/version read from resolution and top-level version field",
			file: "testdata/berry_realistic.yaml",
			want: lockfile.Lock{
				Combined: true,
				Packages: []lockfile.Package{
					{Name: "@algolia/autocomplete-core", Version: "1.17.9"},
					{Name: "lodash", Version: "4.17.21"},
				},
			},
		},
		{
			name: "berry: git dependency reference extracted from the resolution locator's #commit=",
			file: "testdata/berry_git_dependency.yaml",
			want: lockfile.Lock{
				Combined: true,
				Packages: []lockfile.Package{
					{Name: "underscore", Version: "1.13.1", Reference: "cbb48b79fc1205aa04feb03dbc055cdd28a12652"},
				},
			},
		},
		{
			name: "berry: soft-linked workspace member is not reported as a dependency",
			file: "testdata/berry_workspace_link.yaml",
			want: lockfile.Lock{
				Combined: true,
				Packages: []lockfile.Package{
					{Name: "lodash", Version: "4.17.21"},
				},
			},
		},
		{
			name: "berry: metadata-only, no packages",
			file: "testdata/berry_empty.yaml",
			want: lockfile.Lock{Combined: true},
		},
		{
			name:    "berry: malformed YAML",
			file:    "testdata/berry_malformed.yaml",
			wantErr: true,
		},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			data, err := os.ReadFile(tt.file)
			if err != nil {
				t.Fatalf("read fixture: %v", err)
			}

			got, err := Parse(data)

			if tt.wantErr {
				if err == nil {
					t.Fatalf("Parse() error = nil, want an error")
				}
				return
			}
			if err != nil {
				t.Fatalf("Parse() unexpected error: %v", err)
			}

			if got.Combined != tt.want.Combined {
				t.Errorf("Combined = %v, want %v", got.Combined, tt.want.Combined)
			}
			assertPackagesEqual(t, "Packages", got.Packages, tt.want.Packages)
		})
	}
}

func TestIsClassic(t *testing.T) {
	tests := []struct {
		name string
		data string
		want bool
	}{
		{
			name: "classic header present",
			data: "# THIS IS AN AUTOGENERATED FILE. DO NOT EDIT THIS FILE DIRECTLY.\n# yarn lockfile v1\n\nfoo@^1.0.0:\n  version \"1.0.0\"\n",
			want: true,
		},
		{
			name: "berry header, no classic marker",
			data: "# This file is generated by running \"yarn install\" inside your project.\n# Manual changes might be lost - proceed with caution!\n\n__metadata:\n  version: 8\n",
			want: false,
		},
		{
			name: "empty content",
			data: "",
			want: false,
		},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			if got := isClassic([]byte(tt.data)); got != tt.want {
				t.Errorf("isClassic(%q) = %v, want %v", tt.data, got, tt.want)
			}
		})
	}
}

