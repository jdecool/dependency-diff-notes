func TestGitReference(t *testing.T) {
	tests := []struct {
		name     string
		resolved string
		want     string
	}{
		{
			name:     "explicit git+https protocol with commit fragment",
			resolved: "git+https://github.com/adiwajshing/libsignal-node.git#11dbd962ea108187c79a7c46fe4d6f790e23da97",
			want:     "11dbd962ea108187c79a7c46fe4d6f790e23da97",
		},
		{
			name:     "plain git:// protocol with commit fragment",
			resolved: "git://github.com/foo/bar.git#abcdef1234567890abcdef1234567890abcdef12",
			want:     "abcdef1234567890abcdef1234567890abcdef12",
		},
		{
			name:     "github shorthand codeload tarball",
			resolved: "https://codeload.github.com/webpack/tooling/tar.gz/978dc1c9680ef7d79f5f5c02c3439385d7937c39",
			want:     "978dc1c9680ef7d79f5f5c02c3439385d7937c39",
		},
		{
			name:     "registry tarball's trailing hash is a checksum, not a reference",
			resolved: "https://registry.yarnpkg.com/lodash/-/lodash-4.17.21.tgz#679591c564c3bffaae8454cf0b3df370c3d6911c",
			want:     "",
		},
		{
			name:     "git URL without a commit fragment",
			resolved: "git+https://github.com/foo/bar.git",
			want:     "",
		},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			if got := gitReference(tt.resolved); got != tt.want {
				t.Errorf("gitReference(%q) = %q, want %q", tt.resolved, got, tt.want)
			}
		})
	}
}

func TestSplitNameAndLocator(t *testing.T) {
	tests := []struct {
		name        string
		resolution  string
		wantName    string
		wantLocator string
	}{
		{name: "unscoped registry", resolution: "lodash@npm:4.17.21", wantName: "lodash", wantLocator: "npm:4.17.21"},
		{name: "scoped registry", resolution: "@algolia/autocomplete-core@npm:1.17.9", wantName: "@algolia/autocomplete-core", wantLocator: "npm:1.17.9"},
		{
			name:        "git locator",
			resolution:  "underscore@https://github.com/jashkenas/underscore.git#commit=cbb48b79fc1205aa04feb03dbc055cdd28a12652",
			wantName:    "underscore",
			wantLocator: "https://github.com/jashkenas/underscore.git#commit=cbb48b79fc1205aa04feb03dbc055cdd28a12652",
		},
		{name: "quoted", resolution: `"lodash@npm:4.17.21"`, wantName: "lodash", wantLocator: "npm:4.17.21"},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			gotName, gotLocator := splitNameAndLocator(tt.resolution)
			if gotName != tt.wantName {
				t.Errorf("splitNameAndLocator(%q) name = %q, want %q", tt.resolution, gotName, tt.wantName)
			}
			if gotLocator != tt.wantLocator {
				t.Errorf("splitNameAndLocator(%q) locator = %q, want %q", tt.resolution, gotLocator, tt.wantLocator)
			}
		})
	}
}

